package nov7;

public class ComparisonOp {
    public static void main(String[] args) {

        int x = 10;  // assigment operator
        // 10 = 20

        int y = 11;
        boolean result = x >= y; // false  10   >= 11
        boolean result2 = x <= y; // false  , true    10 <= 11

        System.out.println(result);
        System.out.println(result2);


        // >   <    >=   <=  !=   ==


    }
}
