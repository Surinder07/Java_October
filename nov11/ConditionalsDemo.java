package nov11;

public class ConditionalsDemo {
    public static void main(String[] args) {

        int num = 10;

        if(num > 100){
            System.out.println("10 is greater than 100 ");
        }

        else if(num < 100){
            System.out.println(" 10 is less than 100 ");
        }
        else if(num == 100){
            System.out.println("10 is equal to 100 ");
        }
        else {
            System.out.println("Invalid...");
        }
    }
}

